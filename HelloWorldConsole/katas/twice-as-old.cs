namespace HelloWorldConsole.Katas
{
    using System;

    public class TwiceAsOldSolution
    {
        public static int TwiceAsOld(int dadYears, int sonYears)
        {
            return Math.Abs(dadYears - sonYears * 2);
        }
    }
}

